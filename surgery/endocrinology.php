                <h2>Types of Dieseases</h2>
                <h1>Adrenal insufficiency</h1>
                <p>The adrenal gland releases too little of the hormone cortisol and sometimes, aldosterone.</p>
                <p>
                    Addison’s disease is a rare disorder that affects men and women of all ages. Addison’s disease is also referred to as primary adrenal insufficiency. Adrenal insufficiency develops when your adrenal glandsdo not produce enough of the hormone cortisol. This can be due to a problem within the adrenals (called Addison’s disease or primary adrenal insufficiency) or due to a problem with the signal the brain sends to the adrenals instructing them to make cortisol (called secondary adrenal insufficiency).
                    An important distinction for patients is that people with primary adrenal insufficiency (Addison’s disease) usually don’t make enough of the hormone aldosterone; so in addition to taking cortisol replacement, they also need aldosterone replacement. People with secondary adrenal insufficiency are only low on cortisol. Cortisol and aldosterone are just two of the more than 50 hormones the adrenal glands generate. One adrenal gland sits directly above each kidney.</p>
                <h2> Symptoms of Adrenal Insufficiency</h2>
                <p>Symptoms of Addison’s disease and secondary adrenal insufficiency may slowly develop and progress. Common symptoms include:</p>
                <li> Weakness</li>
                <li>Fatigue</li>
                <li>Abdominal pain</li>
                <li>Nausea</li>
                <li>Weight loss</li>
                <li>Low blood pressure</li>
                <li>Darkened skin (in the case of Addison’s disease)</li>
                <li>Salt craving (in the case of Addison’s disease)</li>
                <li>Dizziness upon standing</li>
                <li>Depression</li>

                However, there are different causes of Addison’s disease that can influence the symptoms.
