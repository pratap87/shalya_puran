                <h1>Gigantism (acromegaly) and other growth hormone problems</h1>
                <h2>Acromegaly</h2>
                <p>Acromegaly is a growth disorder caused by too much growth hormone (GH) being released by the pituitary gland, leading to excessive growth.</p>
                <p>When GH is released at normal levels, it promotes healthy growth. Too much of it, though, is a serious—but rare—endocrine disorder. The incidence of acromegaly (how frequent it occurs) is very low: About 3 to 4 people in every 1 million people will be diagnosed with acromegaly every year1.</p>
                <p>Of course, GH is very important in children because it helps their bones and muscles develop (especially during those "growth spurts").</p>
                <p>GH continues to be released, even after you're done growing. In adults, growth hormone influences how much energy you have, how healthy your bones are, how strong you are, and even your overall sense of well-being.
                    Acromegaly is characterized by overgrowth, and it's typically first noticed in the hands and feet. In fact, acro- means extremity in Greek. And the mega part of the word means large (originally in Greek, but English uses the word in the same way). Putting the root words together, acromegaly means large extremities.
                    However, acromegaly affects more than just your extremities. If your pituitary gland is releasing too much growth hormone (find out why in the article on acromegaly causes, your internal organs can be affected, as well as your joints. See a full list of symptoms related to acromegaly .</p>
                <p>In children, acromegaly is called gigantism. Usually, though, acromegaly is diagnosed in adults—especially middle-aged people. This article series will focus on acromegaly, but we do include some information about gigantism.</p>
                
