                 <p>Gynaecology or gynecology (see spelling differences) is the medical practice dealing with the health of the female reproductive systems (vagina, uterus, and ovaries) and the breasts. Outside medicine, the term means "the science of women". Its counterpart is andrology, which deals with medical issues specific to the male reproductive system.
Almost all modern gynaecologists are also obstetricians (see obstetrics and gynaecology). In many areas, the specialities of gynaecology and obstetrics overlap.
</p> 
       <h2>Diseases:</h2>
       <h3>Examples of conditions dealt with by a gynaecologist are:</h3>
       <ul>
           <li> Cancer and pre-cancerous diseases of the reproductive organs including ovaries, fallopian tubes, uterus, cervix, vagina, and vulva</li>
           <li> Incontinence of urine</li>
           <li> Amenorrhoea (absent menstrual periods)</li>
           <li> Dysmenorrhoea (painful menstrual periods)</li>
           <li>Infertility</li>
           <li> Menorrhagia (heavy menstrual periods); a common indication for hysterectomy</li>
           <li>Prolapse of pelvic organs</li>
           <li> Infections of the vagina (vaginitis), cervix and uterus (including fungal, bacterial, viral, and protozoal)</li>
           <li> UTI and Pelvic Inflammatory Disease</li>
           <li> Premenstrual Syndrome</li>
           <li> Other vaginal diseases</li>
       </ul>
       There is some crossover in these areas. For example, a woman with urinary incontinence may be referred to a urologist.
       <h3>Therapies</h3>
       <p>As with all surgical specialties, gynaecologists may employ medical or surgical therapies (or many times, both), depending on the exact nature of the problem that they are treating. Pre- and post-operative medical management will often employ many standard drug therapies, such as antibiotics, diuretics, antihypertensives, and antiemetics. Additionally, gynaecologists make frequent use of specialized hormone-modulating therapies (such as Clomifene citrate and hormonal contraception) to treat disorders of the female genital tract that are responsive to pituitary or gonadal signals.</p>
  <p>Surgery, however, is the mainstay of gynaecological therapy. For historical and political reasons, gynaecologists were previously not considered "surgeons", although this point has always been the source of some controversy. Modern advancements in both general surgery and gynaecology, however, have blurred many of the once rigid lines of distinction. The rise of sub-specialties within gynaecology which are primarily surgical in nature (for example urogynaecology and gynaecological oncology) have strengthened the reputations of gynaecologists as surgical practitioners, and many surgeons and surgical societies have come to view gynaecologists ascomrades of sorts. As proof of this changing attitude, gynaecologists are now eligible for fellowship in both the American College of Surgeons and Royal Colleges of Surgeons, and many newer surgical textbooks include chapters on (at least basic) gynaecological surgery.
Some of the more common operations that gynaecologists perform include:
 </p>
       <ul>
           <li> Dilation and curettage (removal of the uterine contents for various reasons, including completing a partial miscarriage and diagnostic sampling for dysfunctional uterine bleeding refractive to medical therapy)</li>
           <li>Hysterectomy (removal of the uterus)
</li>
           <li>Tubal ligation (a type of permanent sterilization)</li>
           <li>Hysteroscopy (inspection of the uterine cavity)</li>
           <li>6.   Diagnostic laparoscopy – used to diagnose and treat sources of pelvic and abdominal pain; perhaps most famously used to provide a definitive diagnosis of endometriosis.

</li>
           <li> Exploratory laparotomy – may be used to investigate the level of progression of benign or malignant disease, or to assess and repair damage to the pelvic organs.</li>
           <li>Various surgical treatments for urinary incontinence, including cystoscopy and sub-urethral slings.</li>
           <li>Surgical treatment of pelvic organ prolapse, including correction of cystocele and rectocele.
</li>
           <li> Appendectomy – often performed to remove site of painful endometriosis implantation or
 prophylactically (against future acute appendicitis) at the time of hysterectomy or Caesarean section. May also be performed as part of a staging operation for ovarian cancer.
</li>
           <li> Cervical Excision Procedures (including cryosurgery) – removal of the surface of the cervix containing pre-cancerous cells which have been previously identified on Pap smear.</li>
       </ul>
